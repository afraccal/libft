int	ft_isalnum(int c)
{
	int		n;

	n = 1;
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z'))
	{
		return (n);
	}
	else
	{
		n = 0;
	}
	return (n);
}
